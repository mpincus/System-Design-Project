<h3>Cost & Aid</h3>
<p>        SUNY TECH has a growing reputation for providing an exceptional educational experience at
    an affordable cost. In addition to frequent recognition by national organizations for our
    academic quality and commitment to student engagement, we have been named to the Strong nation
    magazine </p>
    <p>list of the 50 Best Values in Public Colleges for 8 years running.
